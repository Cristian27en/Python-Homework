
grade = int(input('Enter the grate: '))

if grade >= 90:
    print('You got an AA!')

elif grade >= 85:
    print('You got an BA!')

elif grade >= 80:
    print('You got an BB!')

elif grade >= 75:
    print('You got an CB!')

elif grade >= 70:
    print('You got an CC!')

elif grade >= 65:
    print('You got an DC!')

elif grade >= 60:
    print('You got an DD!')

else:
    print('You failed the exam')